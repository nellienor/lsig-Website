                    <h1>CHILDHOOD</h1>
                    <p>Born on 1 July 1985, Léa Seydoux is the daughter of businessman Henri Jérôme Seydoux-Fornier de Clausonne and Valérie Schlumberger. She was born in Passy, in the 16th arrondissement of Paris, and grew up in Saint-Germain-des-Prés in the 6th arrondissement.She had a strict Protestant upbringing,but she is not religious.Seydoux is one of seven children. She has three older half-siblings from her mother's first marriage, an older sister, stylist Camille Seydoux from her parents' marriage, and a further two paternal half-brothers from her father's marriage to model Farida Khelfa.</p>
                    <p>Seydoux's parents divorced when she was three years old and they were often away, her mother in Africa and her father on business, which, combined with her large family, meant that she "felt lost in the crowd... I was very lonely as a kid. Really I always had the feeling I was an orphan." Through her family involvement in media and entertainment, Seydoux grew up acquainted with prominent artists such as photographer Nan Goldin, musicians Lou Reed and Mick Jagger and footwear designer Christian Louboutin.For six years, Seydoux went to summer camp in the United States, at the behest of her father, who wanted her to learn to speak English.</p>
