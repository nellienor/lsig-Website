                    <h1>CAREER BEGINNING</h1>
                    <p>Seydoux says that as a child she wanted to become an opera singer, studying music at the Conservatoire de Paris, but eventually her shyness compelled her to drop the idea.It was not until the age of eighteen that she decided to become an actress.One of her friends was an actor, and Seydoux has said: "I found his life wonderful, I thought, 'Oh my god, you can travel, you're free, you can do what you want, you're the boss.'" She fell in love with an actor and decided to become an actress to impress him. She took acting classes at French drama school Les Enfants Terribles,and in 2007 she took further training at New York's Actors Studio with Corinne Blue.</p>
                    <p>In 2005, Seydoux appeared in the music video for Raphaël's single "Ne partons pas fâchés". The following year, Seydoux played her first major screen role as one of the main characters in Sylvie Ayme's Girlfriends (Mes copines). She starred in Nicolas Klotz's short film La Consolation, which was exhibited at the 2007 Cannes Film Festival.
                    In these years, she also did her first work as a model for American Apparel, posing for their Pantytime campaign,and had a role in the films 13 French Street and The Last Mistress.</p>
