                    <h1>FRENCH CINEMA AND HOLLYWOOD EXPANSION</h1>
                    <p>Seydoux came to widespread attention in 2008,when she appeared in Christophe Honoré's The Beautiful Person, a role that earned her the 2009 Chopard Award at the Cannes Film Festival for "Best Upcoming Actress" and a César Award nomination for Most Promising Actress.
                    In 2009, she had a major part in Jessica Hausner's Lourdes,and a small role in her first Hollywood film, Quentin Tarantino's Inglourious Basterds. In 2010 she starred alongside Russell Crowe in Ridley Scott's Robin Hood, playing Isabella of Angoulême. </p>
                    <p id="hidden">In 2011, she played Gabrielle in the romantic comedy Midnight in Paris. She later participated in another Hollywood production, Mission: Impossible – Ghost Protocol, in which she played the assassin Sabine Moreau alongside stars Tom Cruise and Jeremy Renner. She also played Elle in the short film Time Doesn't Stand Still by Benjamin Millepied and Asa Mader . 
                    In 2012, she starred in Farewell, My Queen. The film opened the 62nd Berlin International Film Festival where it was met with critical acclaim.Critics praised director Benoît Jacquot's decision to cast Seydoux in the key role of Sidonie, stating "her luminous but watchful eyes suggest a soul wise beyond her years." Kenneth Turan of the Los Angeles Times wrote that Seydoux was an excellent choice for the part calling her a remarkably versatile young actress and pointed to the stark difference in her characters from her previous roles in Midnight in Paris and Mission: Impossible. That year, Seydoux also filmed Blue Is the Warmest Colour by Abdellatif Kechiche, and Grand Central by Rebecca Zlotowski, both exhibited at the 66th Cannes Film Festival.
                    </p>
